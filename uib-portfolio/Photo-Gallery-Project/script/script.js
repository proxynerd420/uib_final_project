// JavaScript
document.addEventListener('DOMContentLoaded', function() {
    // Finde das Bild-Element, auf das geklickt werden kann
    const pic = document.getElementById('pic1').querySelector('img');

    // Speichere die beiden Bildquellen in einem Array
    const images = [
        '../images/artificer.jpg', // Originalbild
        '../images/artificer2.jpg',
        '../images/artificer3.jpg',
        '../images/artificer4.jpg', // Alternatives Bild
    ];

    // Füge einen Klick-Eventlistener zum Bild hinzu
    pic.addEventListener('click', function() {
        // Überprüfe, welches Bild gerade angezeigt wird
        const currentSrc = pic.getAttribute('src');
        const index = images.indexOf(currentSrc);

        // Wechsle zum nächsten Bild im Array
        const nextIndex = (index + 1) % images.length;
        const nextSrc = images[nextIndex];
        
        // Setze die neue Bildquelle
        pic.setAttribute('src', nextSrc);
    });
});

